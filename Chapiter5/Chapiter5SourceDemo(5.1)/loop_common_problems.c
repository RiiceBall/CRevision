#include <stdio.h>
#include <stdlib.h>

int main(void)
{
	// 循环时容易犯的错误

	// 情况1：忘记初始化循环变量
	int i;
	for (; i < 5; i++)
	{
		// 循环变量没有初始化
	}

	// 情况2：忘记定义退出条件导致死循环
	int i = 0;
	for (;; i++)
	{
		// 缺少退出条件
	}

	// 情况3：忘记递增循环变量导致死循环
	int i = 0;
	for (;i < 9 && i > 1; )
	{
		// 循环变量没有递增
	}

	// 情况4：忘记循环三要素
	// 1、初始化变量
	// 2、退出条件
	// 3、递增变量
	int i = 5;
	for (;;)
	{
		// 标准死循环
	}

	while (1)
	{
		// 标准死循环
	}

	return (0);
}
