#include <stdio.h>
#include <stdlib.h>
#define N 50  // 宏定义N为50，用于指定数组大小(宏定义是常量不能改变值)

int main(void)
{
	// 使用一维数组保存数据

	int nums[25];            // 学生单门学科的成绩
	char array_of_name[30];  // 职员的名字
	double curr_salary[35];  // 某人N个月的工资

	int emp_id[N];  // 创建一个N大小的int数组

	const int SIZE = 100;  // 定义一个常量，用于指定数组大小
	double prices[SIZE];   // 创建一个SIZE大小的double数组

	return (0);
}
